// 랜덤 문제 선택 함수
const getRandomProblems = (problems, count) => {
    const shuffled = shuffleArray([...problems]);
    return shuffled.slice(0, Math.min(count, shuffled.length));
};

// 배열 섞기 함수
const shuffleArray = (array) => {
    const shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
};

// 통계 정보
export const getStatistics = () => {
    const total = mathProblems.length;
    const bySubject = {};
    const byType = {};
    const byDifficulty = {};
    const byExam = {};

    mathProblems.forEach(problem => {
        // 과목별 통계
        bySubject[problem.subject] = (bySubject[problem.subject] || 0) + 1;
        
        // 유형별 통계
        byType[problem.type] = (byType[problem.type] || 0) + 1;
        
        // 난이도별 통계
        byDifficulty[problem.difficulty] = (byDifficulty[problem.difficulty] || 0) + 1;
        
        // 시험별 통계
        byExam[problem.examMonthYear] = (byExam[problem.examMonthYear] || 0) + 1;
    });

    return {
        total,
        bySubject,
        byType,
        byDifficulty,
        byExam
    };
};

// 랜덤 문제 생성 함수
export const getRandomMathProblems = (count = 10, options = {}) => {
    const { difficulty, subject, type, examMonthYear } = options;
    
    let filteredProblems = [...mathProblems];
    
    // 필터링 적용
    if (difficulty) {
        filteredProblems = filteredProblems.filter(p => p.difficulty === difficulty);
    }
    if (subject) {
        filteredProblems = filteredProblems.filter(p => p.subject === subject);
    }
    if (type) {
        filteredProblems = filteredProblems.filter(p => p.type === type);
    }
    if (examMonthYear) {
        filteredProblems = filteredProblems.filter(p => p.examMonthYear === examMonthYear);
    }
    
    // 랜덤 셔플
    const shuffled = filteredProblems.sort(() => Math.random() - 0.5);
    
    // 요청된 개수만큼 반환
    return shuffled.slice(0, Math.min(count, shuffled.length));
};

